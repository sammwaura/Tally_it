public class Employee {

    public String id;
    public String name;
    public String phone;
    public String password;
    public String business_id;


    public Employee(String id, String name, String phone, String password, String business_id) {
        this.id = id;
        this.name = name;
        this.phone = phone;
        this.password = password;
        this.business_id = business_id;



    }

}